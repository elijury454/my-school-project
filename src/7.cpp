// A function that returns the sum of two numbers
int sum(int num1, int num2) {
  return num1 + num2;
}

// Driver code to test the above function
int main() {
  int num1 = 5;
  int num2 = 7;
  int result = sum(num1, num2);
  cout << "Sum of " << num1 << " and " << num2 << " is: " << result << endl;
}
